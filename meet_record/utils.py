"""Shared utility functions for meetscribe.

Formatting helpers that are used across CLI, GUI, transcription, and PDF
modules.  Extracted to eliminate duplication.
"""

from __future__ import annotations


def fmt_elapsed(seconds: float) -> str:
    """Format seconds as HH:MM:SS (always includes hours)."""
    h = int(seconds // 3600)
    m = int((seconds % 3600) // 60)
    s = int(seconds % 60)
    return f"{h:02d}:{m:02d}:{s:02d}"


def fmt_size(nbytes: int) -> str:
    """Format bytes as human-readable size (B / KB / MB / GB)."""
    if nbytes < 1024:
        return f"{nbytes} B"
    elif nbytes < 1024 * 1024:
        return f"{nbytes / 1024:.1f} KB"
    elif nbytes < 1024 * 1024 * 1024:
        return f"{nbytes / (1024 * 1024):.1f} MB"
    else:
        return f"{nbytes / (1024 * 1024 * 1024):.1f} GB"


def fmt_time(seconds: float) -> str:
    """Format seconds as HH:MM:SS (always includes hours)."""
    h = int(seconds // 3600)
    m = int((seconds % 3600) // 60)
    s = int(seconds % 60)
    return f"{h:02d}:{m:02d}:{s:02d}"


def fmt_time_short(seconds: float) -> str:
    """Format seconds as MM:SS, or HH:MM:SS when >= 1 hour."""
    h = int(seconds // 3600)
    m = int((seconds % 3600) // 60)
    s = int(seconds % 60)
    if h > 0:
        return f"{h:02d}:{m:02d}:{s:02d}"
    return f"{m:02d}:{s:02d}"


def fmt_srt_time(seconds: float) -> str:
    """Format seconds as SRT timestamp HH:MM:SS,mmm."""
    h = int(seconds // 3600)
    m = int((seconds % 3600) // 60)
    s = int(seconds % 60)
    ms = int((seconds % 1) * 1000)
    return f"{h:02d}:{m:02d}:{s:02d},{ms:03d}"
